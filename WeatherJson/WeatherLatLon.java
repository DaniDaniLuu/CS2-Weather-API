package WeatherJson;
public class WeatherLatLon {
    private double lat;
    private double lon;

    public double getLat(){
        return this.lat;
    }

    public void setLat(double lat){
        this.lat = lat;
    }

    public double getLon(){
        return this.lon;
    }

    public void setLon(double lon){
        this.lon = lon;
    }

}
